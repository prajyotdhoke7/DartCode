/* take a number and print whether it is +ve or -ve
input: var x=5;
output: 5 is a positive number;
input: var x=-9;
output: -9 is a negative number;
*/

void main() {
  var x = 5;
  if (x > 0) {
    print("$x is a positive number");
  } else {
    print("$x is a negative number");
  }
  var y = -9;
  if (y > 0) {
    print("$y is a positive number");
  } else {
    print("$y is a negative number");
  }
}
