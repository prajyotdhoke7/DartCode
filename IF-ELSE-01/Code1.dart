/* print whether it is less than 10 or greater than 10.
i/p-var x=5;
o/p-5 is less than 10;
i/p- var x=16;
o/p- 16 is greater than 10;
*/

void main() {
  var x = 5;
  if (x < 10) {
    print("$x is less than 10");
  } else {
    print("$x is greater than 10");
  }
  var y = 16;
  if (y > 10) {
    print("$y is less than 10");
  } else {
    print("$y is greater than 10");
  }
}
